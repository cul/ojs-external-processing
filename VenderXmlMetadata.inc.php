		$articleId = $dom->getElementsByTagName('article-id')->item(0)->nodeValue;
				
		$articleDao = & DAORegistry::getDAO('ArticleDAO');		
		$article = $articleDao->getArticle($articleId, $journalId);

		$title = $dom->getElementsByTagName('article-title')->item(0)->nodeValue;
		$article->setTitle($title, Locale::getLocale());

		$abstract = $dom->getElementsByTagName('abstract')->item(0)->nodeValue;
		$article->setAbstract($abstract, Locale::getLocale());

		VenderXmlMetadata::processAuthors(&$dom, &$article);
		
		$articleDao->updateArticle(&$article);
	}
	
	function processAuthors(&$dom, &$article){

 		$articleAuthors = $article->getAuthors();

		$contributers = $dom->getElementsByTagName('contrib');
		foreach($contributers as $contributer){
			
			$contributerType = $contributer->getAttribute('contrib-type');
			if($contributerType == "author"){
				//$lastName = $contributer->getElementsByTagName('surname')->item(0)->nodeValue;
				//$firstName = $contributer->getElementsByTagName('given-names')->item(0)->nodeValue;
				//$email = $contributer->getElementsByTagName('email')->item(0)->nodeValue;
				$authorId = $contributer->getAttribute('id');
				
				$articleAuthor = $article->getAuthor($authorId);

				if($articleAuthor){
                                        echo "========== CW start to modify author with id: " . $authorId. " \n";
					VenderXmlMetadata::updateExistedAuthor($contributer, &$articleAuthors);
				}else{
                                        echo "========== CW start to add new author with id: " . $authorId. " \n";
					VenderXmlMetadata::addNewAuthor($contributer, &$article);
				}
			}
		}		
	}
	
	function updateExistedAuthor($contributer, &$articleAuthors){

		foreach($articleAuthors as $authorOld){
			if($authorOld->getId() == $contributer->getAttribute('id')){
				break;
			}
		}
		
		VenderXmlMetadata::setValuesToAuthor($contributer, &$authorOld);
		
		echo "========== CW modified author with id: " . $authorOld->getId() . " \n";
	}

	function addNewAuthor($contributer, &$article){
		
		$author = new Author();
		VenderXmlMetadata::setValuesToAuthor($contributer, &$author);
		$article->addAuthor($author);
		
		echo "========== CW added new author: " . $author->getFirstName() . " " . $author->getLastName() . "  \n";
	}	


	function setValuesToAuthor($contributer, &$author){
		
		if($contributer->getElementsByTagName('surname') != NULL && $contributer->getElementsByTagName('surname')->length != 0){
			$author->setLastName($contributer->getElementsByTagName('surname')->item(0)->nodeValue);
		}
		if($contributer->getElementsByTagName('given-names') != NULL && $contributer->getElementsByTagName('given-names')->length != 0){
			$author->setFirstName($contributer->getElementsByTagName('given-names')->item(0)->nodeValue);
		}
		if($contributer->getElementsByTagName('email') != NULL && $contributer->getElementsByTagName('email')->length != 0){
			$author->setEmail($contributer->getElementsByTagName('email')->item(0)->nodeValue);
		} else {
			$author->setEmail("");
		}
		if($contributer->getElementsByTagName('aff') != NULL && $contributer->getElementsByTagName('aff')->length != 0){
			$author->setAffiliation($contributer->getElementsByTagName('aff')->item(0)->nodeValue, 'en_US');
		}
		if($contributer->getElementsByTagName('initials') != NULL && $contributer->getElementsByTagName('initials')->length != 0){
			$author->setMiddleName($contributer->getElementsByTagName('initials')->item(0)->nodeValue);
		}
	}
	
	
